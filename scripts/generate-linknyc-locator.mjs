import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const LOCATOR_URL = "https://www.link.nyc/find-a-link.html";
const KML_URL = "https://www.link.nyc/map/LinkNYC.kml";
const OUTPUT_PATH = "../src/data/linknycLocator.ts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outputPath = path.resolve(__dirname, OUTPUT_PATH);

const response = await fetch(KML_URL);
if (!response.ok) {
  throw new Error(`Failed to fetch ${KML_URL}: ${response.status}`);
}

const kml = await response.text();
const generatedAt = new Intl.DateTimeFormat("en-CA", {
  timeZone: "America/New_York",
  year: "numeric",
  month: "2-digit",
  day: "2-digit",
}).format(new Date());
const placemarks = [...kml.matchAll(/<Placemark[\s\S]*?<\/Placemark>/g)].map(
  (match) => match[0],
);
const rows = placemarks
  .map(parsePlacemark)
  .filter((row) => row.borough === "Manhattan")
  .sort((a, b) => b.latitude - a.latitude || a.longitude - b.longitude);

const pins = rows.map((row) => {
  const available = row.description.toLowerCase().includes("available");
  const productGroup = row.productGroup.toLowerCase() === "link5g" ? "Link5G" : "Link";

  return {
    id: `linknyc-locator-${slug(row.siteId || `${row.address}-${row.latitude}-${row.longitude}`)}`,
    name: `LinkNYC locator: ${row.address || row.crossStreets}`,
    shortName: productGroup === "Link5G" ? "Link5G" : "Link locator",
    category: "linknyc-locator",
    stage: stageForLatitude(row.latitude),
    role: available ? "optional" : "recon",
    latitude: row.latitude,
    longitude: row.longitude,
    radiusMeters: productGroup === "Link5G" ? 90 : 75,
    address: row.address || row.crossStreets,
    description:
      productGroup === "Link5G"
        ? "Generated from LinkNYC Find a Link locator data for Link5G smart poles."
        : "Generated from LinkNYC Find a Link locator data for kiosk Wi-Fi.",
    wifi: {
      provider: "LinkNYC - Citybridge",
      ssids: available ? ["LinkNYC Free Wi-Fi"] : ["LinkNYC Free Wi-Fi coming soon"],
      accessType: available ? "free" : "needs-recon",
      locationType: productGroup === "Link5G" ? "Link5G smart pole" : "LinkNYC kiosk",
      statusLabel: available ? "Free Wi-Fi available" : "Installed; Wi-Fi coming soon",
      statusDate: generatedAt,
      remarks: `${row.crossStreets || "LinkNYC locator point"}; generated from LinkNYC Find a Link KML.`,
      sourceId: row.siteId ? `LinkNYC locator ${row.siteId}` : "LinkNYC locator KML",
      liveStatus: available ? "up" : "not-live",
    },
    sourceLinks: [
      { label: "LinkNYC Find a Link", url: LOCATOR_URL },
      { label: "LinkNYC KML", url: KML_URL },
    ],
    mapsQuery: `${row.address || row.crossStreets} Manhattan NY`,
    pathways: pathwaysForCoordinates(row.latitude, row.longitude),
    metadata: {
      sourceDataset: "LinkNYC Find a Link KML",
      sourceGeneratedAt: generatedAt,
      linknycLocator: "true",
      linknycSiteId: row.siteId,
      linknycProductGroup: productGroup,
      linknycAvailability: available ? "available" : "coming-soon",
      linknycDisplay: row.display,
      linknycBorough: row.borough,
      nearestCrossStreets: row.crossStreets,
    },
  };
});

const file = `import type { TourPin } from "../types";

// Generated by scripts/generate-linknyc-locator.mjs from ${KML_URL}
// Source locator page: ${LOCATOR_URL}
// Generated on ${generatedAt}; Manhattan records only.
export const linknycLocatorPins: TourPin[] = ${JSON.stringify(pins, null, 2)};
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${file}\n`);

console.log(
  `Wrote ${pins.length} Manhattan LinkNYC locator pins to ${path.relative(
    process.cwd(),
    outputPath,
  )}`,
);

function parsePlacemark(placemark) {
  const coordinates = tagText(placemark, "coordinates")
    .split(",")
    .map((value) => Number(value));

  return {
    crossStreets: tagText(placemark, "name"),
    address: tagText(placemark, "address"),
    borough: tagText(placemark, "phoneNumber"),
    description: tagText(placemark, "description"),
    siteId: simpleData(placemark, "siteId"),
    display: simpleData(placemark, "display"),
    productGroup: simpleData(placemark, "productGroup"),
    longitude: coordinates[0],
    latitude: coordinates[1],
  };
}

function tagText(source, tag) {
  const match = source.match(new RegExp(`<${tag}[^>]*>([\\s\\S]*?)<\\/${tag}>`));
  return decode(match?.[1] ?? "");
}

function simpleData(source, name) {
  const match = source.match(
    new RegExp(`<SimpleData\\s+name=["']${name}["']>([\\s\\S]*?)<\\/SimpleData>`),
  );
  return decode(match?.[1] ?? "");
}

function decode(value) {
  return value
    .replace(/<!\[CDATA\[|\]\]>/g, "")
    .replace(/&amp;/g, "&")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .trim();
}

function slug(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function stageForLatitude(latitude) {
  if (latitude >= 40.758) {
    return "uptown";
  }

  if (latitude <= 40.73) {
    return "downtown";
  }

  if (latitude < 40.738) {
    return "union-square";
  }

  return latitude >= 40.748 ? "origin" : "midtown";
}

function pathwaysForCoordinates(latitude, longitude) {
  const crossTownPathway =
    longitude < -73.994 ? "west" : longitude > -73.978 ? "east" : "spine";
  const pathways = [crossTownPathway];

  if (latitude < 40.733 || latitude > 40.758) {
    pathways.push("transit");
  }

  return Array.from(new Set(pathways));
}
