function wifiForExpandedRecord(record: ExpandedWifiRecord): TourPin["wifi"] {
  const linkSiteId =
    record.provider === "LinkNYC - Citybridge"
      ? currentLinkStatusByAddress[record.address.toUpperCase()]
      : undefined;

  return {
    provider: record.provider,
    ssids: Array.isArray(record.ssid) ? record.ssid : [record.ssid],
    accessType: record.accessType,
    locationType: record.locationType,
    statusLabel: linkSiteId ? "Live; Wi-Fi up" : record.statusLabel,
    statusDate: linkSiteId ? LINK_STATUS_GENERATED_AT : record.statusDate,
    remarks: record.remarks,
    sourceId: linkSiteId ? `LinkNYC site ${linkSiteId}` : record.sourceId,
    liveStatus: linkSiteId ? "up" : (record.liveStatus ?? "unknown"),
  };
}

function linksForProvider(record: ExpandedWifiRecord) {
  if (record.provider === "NYPL") {
    return [
      { label: "NYPL wireless access", url: NYPL_WIFI },
      { label: "NYC Wi-Fi Hotspot Locations", url: NYC_WIFI },
    ];
  }

  if (record.provider === "LinkNYC - Citybridge") {
    return [
      { label: "LinkNYC Find a Link", url: LINK_LOCATOR },
      { label: "LinkNYC how to connect", url: LINK_CONNECT },
      { label: "LinkNYC Kiosk Status", url: LINK_STATUS },
      { label: "NYC Wi-Fi Hotspot Locations", url: NYC_WIFI },
    ];
  }

  if (record.category === "park") {
    return [
      { label: "NYC Wi-Fi Hotspot Locations", url: NYC_WIFI },
      { label: "NYC Parks Wi-Fi facilities", url: PARKS_WIFI },
    ];
  }

  if (record.category === "public-service") {
    return [
      { label: "NYC Wi-Fi Hotspot Locations", url: NYC_WIFI },
      { label: "NYC MOPD provider page", url: MOPD },
    ];
  }

  return [{ label: "NYC Wi-Fi Hotspot Locations", url: NYC_WIFI }];
}

