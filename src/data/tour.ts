function stageForLatitude(latitude: number): TourPin["stage"] {
  if (latitude >= 40.758) {
    return "uptown";
  }

  if (latitude <= 40.73) {
    return "downtown";
  }

  if (latitude < 40.738) {
    return "union-square";
  }

  return latitude >= 40.748 ? "origin" : "midtown";
}

function pathwaysForCoordinates(latitude: number, longitude: number): TourPathway[] {
  const crossTownPathway =
    longitude < -73.994 ? "west" : longitude > -73.978 ? "east" : "spine";
  const pathways: TourPathway[] = [crossTownPathway];

  if (latitude < 40.733 || latitude > 40.758) {
    pathways.push("transit");
  }

  return Array.from(new Set(pathways));
}

